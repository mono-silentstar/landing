        <h3 class="diss-title">On the Persistence of Bookmarks in Digital Spaces</h3>
        <p class="diss-meta"><em>Submitted in partial fulfilment of requirements, 2017</em></p>
        <p>This thesis investigates how users organise, abandon, and rediscover
        saved content across platforms. Through mixed-methods research combining
        browser data analysis and semi-structured interviews, it maps the lifecycle
        of the digital bookmark from hopeful curation to quiet neglect.</p>
